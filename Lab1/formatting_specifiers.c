#include<stdio.h>
    //different types of formatting specifiers
void main(){
    int x,y;//variable declaration
    x=15;//variable intialization
    printf("the value of x and y is %d,%d\n",x,y);
    float z=1206,n=18.5;//variable intialization with declaration
    printf("the value of z is %f\n",z);
    printf("the value of n is %f \n",n);
    char g;
    printf("the value of g is %c\n",g);
    char a[20]="hello world";
    printf("%s\n",a);

}